weather = 'sunny'

case weather
	when 'sunny'
		puts "wear shorts and maybe sunscreen!"
	when 'rainy'
		puts "you may want to wear a jacket today! and pack an umbrella."
	when 'foggy'
		puts "bring a flashlight with you so cars can see you!"
	when 'cloudy'
	 	puts "Pack a light jacket in case the weather intensifies."
end