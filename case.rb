fav_color = 'green'

case fav_color
	when 'red'
		puts "Red like fire!"
	when 'orange'
		puts "Orange like cantaloupe!"
	when 'yellow'
		puts "Yellow daffodils are so pretty in the spring!"
	when 'pink'
		puts "Pink flamingos are super cool."
	when 'blue'
		puts "Blue like the sky!"
	when 'purple'
		puts "Purple plums are the tastiest"
	when 'green'
		puts "Have you been to the Emerald City?"
	else
		puts "Hmm, well I don't know what that color is!"
end
		
		